/**
 * Sampler over a digit grid: returns (u, v) -> ink 0..1 with
 * coordinates in 0..1 and edges clamped (sampling outside the grid
 * extends the border, so grounds and waters continue naturally).
 *
 * Interpolation fractions are shaped by a double smoothstep before
 * blending: plateaus keep their authored tone exactly (a 2 stays a 2 —
 * plain contrast curves erased the floors and waters), while the
 * transition between tones narrows to about a character — the picture
 * stays pixel-art crisp instead of dissolving into gray halos.
 */
export function gridSampler(rows) {
  const H = rows.length;
  const W = rows[0].length;
  const at = (x, y) => {
    x = Math.max(0, Math.min(W - 1, x));
    y = Math.max(0, Math.min(H - 1, y));
    return Number(rows[y][x]) / 9;
  };
  const ss = (t) => t * t * (3 - 2 * t);
  return (u, v) => {
    const gx = u * W - 0.5;
    const gy = v * H - 0.5;
    const x0 = Math.floor(gx);
    const y0 = Math.floor(gy);
    const fx = ss(ss(gx - x0));
    const fy = ss(ss(gy - y0));
    return at(x0, y0) * (1 - fx) * (1 - fy) + at(x0 + 1, y0) * fx * (1 - fy)
      + at(x0, y0 + 1) * (1 - fx) * fy + at(x0 + 1, y0 + 1) * fx * fy;
  };
}
